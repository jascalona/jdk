public class Variables_01 {
    public static void main(String[] args) {

        //declaracion de variables en linea
        String nameI = "Jose", nameII = "Abraham", surNameI = "Escalona", surNameII = "Blanco", using = "Jescalona";
        System.out.println(nameI + " " + nameII + " " + surNameI + " " + surNameII + " " + using);
        System.out.println("________________________________________");

        //declaracion de variables en columnas
        String name1 = "Jose";
        String name2 = "Abraham";
        String surname1 = "Escalona";
        String surname2 = "Blanco";
        String using1 = "Jescalona";
        System.out.println(name1 + " " + name2 + " " + surname1  + " " + surname2 + " " + using1);
        System.out.println("________________________________________");
    }
}
